package com.example.adminservice.modul.crud.create;

public class inputuang {
    private String id_keuangan;
    private String nama_pemberi;
    private String no_hp;
    private String alamat;
    private String jumlah;
    private String harian;


    public String getId_keuangan() {
        return id_keuangan;
    }

    public void setId_keuangan(String id_keuangan) {
        this.id_keuangan = id_keuangan;
    }

    public String getNama_pemberi() {
        return nama_pemberi;
    }

    public void setNama_pemberi(String nama_pemberi) {
        this.nama_pemberi = nama_pemberi;
    }

    public String getNo_hp() {
        return no_hp;
    }

    public void setNo_hp(String no_hp) {
        this.no_hp = no_hp;
    }

    public String getAlamat() {
        return alamat;
    }

    public void setAlamat(String alamat) {
        this.alamat = alamat;
    }

    public String getJumlah() {
        return jumlah;
    }

    public void setJumlah(String jumlah) {
        this.jumlah = jumlah;
    }

    public String getHarian() {
        return harian;
    }

    public void setHarian(String harian) {
        this.harian = harian;
    }
}
