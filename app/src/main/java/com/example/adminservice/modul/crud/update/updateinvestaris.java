package com.example.adminservice.modul.crud.update;

public class updateinvestaris {
    private String id;
    private String nama_aset;
    private String jumlah;
    private String satuan;
    private String kondisi;
    private String keterngan;

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getNama_aset() {
        return nama_aset;
    }

    public void setNama_aset(String nama_aset) {
        this.nama_aset = nama_aset;
    }

    public String getJumlah() {
        return jumlah;
    }

    public void setJumlah(String jumlah) {
        this.jumlah = jumlah;
    }

    public String getSatuan() {
        return satuan;
    }

    public void setSatuan(String satuan) {
        this.satuan = satuan;
    }

    public String getKondisi() {
        return kondisi;
    }

    public void setKondisi(String kondisi) {
        this.kondisi = kondisi;
    }

    public String getKeterngan() {
        return keterngan;
    }

    public void setKeterngan(String keterngan) {
        this.keterngan = keterngan;
    }
}
